function spot(
  id: string,
  name: string,
  code: string,
  assetType: AssetType,
  initialConsonants: string,
  aliases: string[] = [],
  sector = "대형주"
): Instrument {
  return {
    id,
    marketType: "spot",
    assetType,
    name,
    code,
    displayName: name,
    exchange: assetType === "stock" ? "KOSPI" : "ETF",
    sector,
    initialConsonants,
    aliases,
    isActive: true,
    createdAt: now,
    updatedAt: now
  };
}

function future(
  id: string,
  name: string,
  code: string,
  initialConsonants: string,
  aliases: string[],
  multiplier: number,
  tickSize: number,
  tickValue: number
): Instrument {
  return {
    id,
    marketType: "futures",
    assetType: "index_futures",
    name,
    code,
    displayName: name,
    exchange: "KRX",
    sector: "지수선물",
    initialConsonants,
    aliases,
    multiplier,
    tickSize,
    tickValue,
    feeRate: 0.00003,
    contractMonth: "최근월물",
    isActive: true,
    createdAt: now,
    updatedAt: now
  };
}

